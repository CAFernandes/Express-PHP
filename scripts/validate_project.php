require_once __DIR__ . '/../vendor/autoload.php';

class ProjectValidator
{
    private $errors = [];
    private $warnings = [];
    private $passed = [];

    public function validate()
    {
        echo "üîç Validando projeto Express PHP...\n\n";

        // Testes estruturais
        $this->validateStructure();
