    private function validateSecurity()
    {
        echo "üîí Validando configura√ß√µes de seguran√ßa...\n";

        // Verificar se arquivos sens√≠veis n√£o est√£o sendo commitados
        $sensitiveFiles = [
            '.env' => 'Arquivo de environment',
            'config/database.php' => 'Configura√ß√£o de banco local',
            'composer.lock' => 'Lock file do composer (se deve ser commitado depende do projeto)'
        ];

        foreach ($sensitiveFiles as $file => $description) {
            if (file_exists($file)) {
                $this->warnings[] = "{$description} presente ({$file}) - verifique se deve ser commitado";
            }
        }

        // Verificar se .gitignore est√° configurado corretamente
        if (file_exists('.gitignore')) {
            $gitignore = file_get_contents('.gitignore');
            $requiredEntries = ['/vendor/', '.env', '*.log'];

            foreach ($requiredEntries as $entry) {
                if (strpos($gitignore, $entry) !== false) {
                    $this->passed[] = "Entrada '{$entry}' presente no .gitignore";
                } else {
                    $this->warnings[] = "Entrada '{$entry}' ausente no .gitignore";
                }
            }
        } else {
            $this->errors[] = "Arquivo .gitignore n√£o encontrado";
        }

        // Verificar se .env.example existe
        if (file_exists('.env.example')) {
            $this->passed[] = "Arquivo .env.example presente para refer√™ncia";
        } else {
            $this->warnings[] = "Arquivo .env.example n√£o encontrado - recomendado para projetos";
        }

        // Verificar configura√ß√µes de seguran√ßa no c√≥digo
        $securityFiles = glob('src/Middlewares/Security/*.php');
        if (count($securityFiles) >= 2) {
            $this->passed[] = "M√∫ltiplos middlewares de seguran√ßa implementados";
        } else {
            $this->warnings[] = "Poucos middlewares de seguran√ßa encontrados";
        }

        echo "‚úÖ Seguran√ßa validada\n\n";
    }

    private function validateOpenApiFeatures()
    {
        echo "üìö Validando recursos OpenAPI/Swagger...\n";

        // Verificar se OpenApiExporter existe
        if (class_exists('Express\\Utils\\OpenApiExporter')) {
            $this->passed[] = "OpenApiExporter carregado";

            // Testar export b√°sico
            try {
                if (class_exists('Express\\Routing\\Router')) {
                    $docs = Express\Utils\OpenApiExporter::export('Express\\Routing\\Router');
                    if (is_array($docs) && isset($docs['openapi'])) {
                        $this->passed[] = "OpenApiExporter pode gerar documenta√ß√£o";

                        if ($docs['openapi'] === '3.0.0') {
                            $this->passed[] = "OpenApiExporter gera OpenAPI 3.0.0";
                        } else {
                            $this->warnings[] = "OpenApiExporter pode n√£o estar usando OpenAPI 3.0.0";
                        }
                    } else {
                        $this->errors[] = "OpenApiExporter n√£o gera documenta√ß√£o v√°lida";
                    }
                } else {
                    $this->warnings[] = "Router n√£o encontrado para testar OpenApiExporter";
                }
            } catch (Exception $e) {
                $this->errors[] = "Erro ao testar OpenApiExporter: " . $e->getMessage();
            }
        } else {
            $this->errors[] = "OpenApiExporter n√£o encontrado";
        }

        // Verificar se o README principal menciona OpenAPI
        if (file_exists('README.md')) {
            $readme = file_get_contents('README.md');
            if (strpos($readme, 'OpenAPI') !== false || strpos($readme, 'Swagger') !== false) {
                $this->passed[] = "README principal menciona OpenAPI/Swagger";

                if (strpos($readme, 'OpenApiExporter') !== false) {
                    $this->passed[] = "README explica como usar OpenApiExporter";
                } else {
                    $this->warnings[] = "README pode n√£o explicar como usar OpenApiExporter";
                }
            } else {
                $this->warnings[] = "README principal pode n√£o mencionar recursos OpenAPI";
            }
        }

        echo "‚úÖ Recursos OpenAPI validados\n\n";
    }

    private function generateReport()
    {
        echo "üìä RELAT√ìRIO DE VALIDA√á√ÉO\n";
        echo str_repeat("=", 50) . "\n\n";

        echo "‚úÖ SUCESSOS (" . count($this->passed) . "):\n";
        foreach ($this->passed as $pass) {
            echo "  ‚úì {$pass}\n";
